# Aprendendo a utilizar tipos primitivos e data output
# int para números inteiros, float para números com virgula (numeros reais)
# float é usado para numeros quebrados (com virgula)
# bool (boleanos) para valor Verdadeiro ou Falso (True or False)
# str é usado para passar uma mensagem ou melhor dizendo para passar algo entre 'ASPAS'
# .format(nome)) é usado para formatar algo para dentro dos conchetes

# Exemplos

a = int(input('Digite um valor '))
print(a)

n = float(input('Digite um valor '))
print(n)

b = bool(input('Digite um valor '))
print(b)

c = str(input('Digite um valor '))
print(c)

